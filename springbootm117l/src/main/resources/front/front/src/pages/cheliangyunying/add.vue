        this.type = type;
        if(type=='cross'){
          var obj = JSON.parse(localStorage.getItem('crossObj'));
          for (var o in obj){
            if(o=='cheliangbianhao'){
              this.ruleForm.cheliangbianhao = obj[o];
              this.ro.cheliangbianhao = true;
              continue;
            }
            if(o=='cheliangpinpai'){
              this.ruleForm.cheliangpinpai = obj[o];
              this.ro.cheliangpinpai = true;
              continue;
